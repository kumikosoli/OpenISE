package com.entity;

public class Usera {

	private String ubh;
	private String xm;
	private String xb;
	private String dh;
	private String jtdz;
	private String upwd;


	public Usera(String ubh, String xm, String xb, String dh, String jtdz, String upwd) {
		super();
		this.ubh = ubh;
		this.xm = xm;
		this.xb = xb;
		this.dh = dh;
		this.jtdz = jtdz;
		this.upwd = upwd;
	}







	public String getUpwd() {
		return upwd;
	}







	public void setUpwd(String upwd) {
		this.upwd = upwd;
	}







	public String getUbh() {
		return ubh;
	}







	public void setUbh(String ubh) {
		this.ubh = ubh;
	}







	public String getXm() {
		return xm;
	}







	public void setXm(String xm) {
		this.xm = xm;
	}







	public String getXb() {
		return xb;
	}







	public void setXb(String xb) {
		this.xb = xb;
	}







	public String getDh() {
		return dh;
	}







	public void setDh(String dh) {
		this.dh = dh;
	}







	public String getJtdz() {
		return jtdz;
	}







	public void setJtdz(String jtdz) {
		this.jtdz = jtdz;
	}







	public Usera() {
	}

}
